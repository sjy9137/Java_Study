package variable;

public class IntEaxmple {
	public static void main(String[] args) {
		int var1 = 10; // 10진수
		int var2 = 012; // 8진수
		int var3 = 0xA; // 16진수
		long var4 = 10000000000000L;

		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		System.out.println(var4);
	}
}
