package variable;

public class FloatDoubleExample {
	public static void main(String[] args) {
		double var1 = 3.14;
		float  var2 = 3.14f;
		
		System.out.println(var1);
		System.out.println(var2);
		
		double var7 = 3e6;
		float var8 = 3e6F;
		double var9 = 2e-3;
		
		System.out.println(var7);
		System.out.println(var8);
		System.out.println(var9);
		
	}
}
